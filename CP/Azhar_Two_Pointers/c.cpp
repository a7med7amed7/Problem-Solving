void solve()
{
    int n;
    cin >> n;
    int arr[n];
    for (int i = 0; i < n; i++)
    {
        cin >> arr[i];
    }
    sort(arr, arr + n);
    int l = 0, r = n - 1;
    int ans = INT_MAX;
    while (l <= r)
    {
        if (abs(arr[l] + arr[r]) <= ans)
        {
            ans = (abs(arr[l] + arr[r]));
            if (arr[l] < 0)
                r--;
            else
                l++;
        }
        else
        {
            r--;
        }
    }
    cout << ans;
}

